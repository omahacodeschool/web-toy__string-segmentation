def valid_word?(word)

  dictionary = ["i", "love", "you", "lightbulbs", "human", "pressure", "puzzle", "puzzles", "magic", "saddness", "pushing", "frost", "frostbite", "peace", "truth", "art", "with", "happiness", "becomes", "clear", "everything", "is", "beautiful", "and", "nothing", "hurt", "was"]

  dictionary.include?(word)
end